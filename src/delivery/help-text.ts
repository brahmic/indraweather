export function formatHelpHtml(scheduleTimes: string[], subscriptionEnabled = false): string {
  return [
    "<b>Индира · погода Кемь — Кандалакша</b>",
    "",
    subscriptionEnabled
      ? `Подписка включена. Бюллетени приходят ежедневно в ${scheduleTimes.join(", ")} МСК.`
      : `Бюллетени приходят ежедневно в ${scheduleTimes.join(", ")} МСК после <code>/start</code>.`,
    "",
    "<b>Прогноз</b>",
    "<code>/weather</code> — актуальная сводка, снимки и условия на маршруте.",
    "<code>/details</code> — ECMWF и GFS по отдельности.",
    "<code>/forecast</code> — выбрать точку и получить прогноз на 5 дней с отдельными ECMWF и GFS.",
    "Диапазоны в сводке показывают границы моделей, а не среднее значение.",
    "",
    "<b>Карта и снимки</b>",
    "<code>/map</code> — личный охват карты: стрелки сдвигают её, + и − меняют масштаб.",
    "Он применяется к статичным снимкам <code>/weather</code>, <code>/clouds</code> и <code>/radar</code>.",
    "Плановые бюллетени остаются в общем охвате.",
    "Днём спутник показывает естественные цвета, ночью — инфракрасный канал.",
    "",
    "<b>Диагностика</b>",
    "<code>/clouds</code> — типы облаков днём, туман и низкая облачность ночью.",
    "<code>/radar</code> — поверхность по Sentinel-1 сквозь облака; ледяную обстановку нужно трактовать осторожно.",
    "",
    "<b>Анимации</b>",
    "Ролики показывают до 12 часов движения облачности и появляются после трёх успешных кадров.",
    "Для личной карты ролик приходит отдельно после <code>/weather</code> или <code>/clouds</code>; настройка <code>/map</code> сама ничего не отправляет.",
    "",
    "<code>/points</code> — контрольные точки · <code>/status</code> — время последнего обновления · <code>/stop</code> — отключить уведомления.",
  ].join("\n");
}
